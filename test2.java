public class  StringCase{
	public static void main(String[] args) {
		
		String text1 =  "ABCD";
		String text2 =  "abcd";
		String text3 = "   Hello World!   ";
		
		System.out.print(text3.trim());
		System.out.println(text1.toLowerCase());
		System.out.print(text3.trim());
		System.out.println(text2.toUpperCase());
		System.out.print(text3.trim());

	}
}
